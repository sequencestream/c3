// Walk web/dist and generate server/src/static-embed.ts — a module that uses
// Bun's `import ... with { type: 'text' }` attribute so the bytes are inlined
// into the `bun build --compile` binary.
import { readdirSync, statSync, writeFileSync, existsSync } from 'node:fs'
import { resolve, relative, posix, dirname, extname } from 'node:path'
import { fileURLToPath } from 'node:url'

const here = dirname(fileURLToPath(import.meta.url))
const webDist = resolve(here, '..', '..', 'web', 'dist')
const out = resolve(here, '..', 'src', 'static-embed.ts')

if (!existsSync(resolve(webDist, 'index.html'))) {
  console.error(
    `[generate-static-embed] web/dist/index.html missing — run \`pnpm -F @ccc/web build\` first.`,
  )
  process.exit(1)
}

const MIME = {
  '.html': 'text/html; charset=utf-8',
  '.js': 'application/javascript; charset=utf-8',
  '.mjs': 'application/javascript; charset=utf-8',
  '.css': 'text/css; charset=utf-8',
  '.svg': 'image/svg+xml; charset=utf-8',
  '.json': 'application/json; charset=utf-8',
  '.txt': 'text/plain; charset=utf-8',
  '.map': 'application/json; charset=utf-8',
}

function walk(dir) {
  const out = []
  for (const entry of readdirSync(dir)) {
    const full = resolve(dir, entry)
    if (statSync(full).isDirectory()) {
      out.push(...walk(full))
    } else {
      out.push(full)
    }
  }
  return out
}

const files = walk(webDist).sort()
if (files.length === 0) {
  console.error('[generate-static-embed] no files in web/dist')
  process.exit(1)
}

const imports = []
const entries = []

files.forEach((file, i) => {
  const ext = extname(file)
  const mime = MIME[ext]
  if (!mime) {
    console.error(
      `[generate-static-embed] no MIME mapping for "${ext}" (file: ${file}). Extend MIME table.`,
    )
    process.exit(1)
  }
  // Import path relative to server/src/static-embed.ts → ../../web/dist/...
  const importRel =
    './' + posix.join('..', '..', 'web', 'dist', relative(webDist, file).split(/[\\/]/).join('/'))
  const ident = `asset${i}`
  imports.push(`import ${ident} from '${importRel}' with { type: 'text' };`)
  // URL path: leading slash, posix separators
  const urlPath = '/' + relative(webDist, file).split(/[\\/]/).join('/')
  entries.push(`  ['${urlPath}', { body: ${ident}, mime: '${mime}' }],`)
})

const body = `// AUTO-GENERATED by server/scripts/generate-static-embed.mjs — do not edit.
// Source: web/dist/. Re-run \`pnpm -F @ccc/server pkg\` to refresh.
// @ts-nocheck — \`with { type: 'text' }\` attribute is a Bun extension; TS lacks declarations.
${imports.join('\n')}

export const STATIC_ASSETS: ReadonlyMap<string, { body: string; mime: string }> = new Map([
${entries.join('\n')}
]);
`

writeFileSync(out, body)
console.log(`[generate-static-embed] wrote ${files.length} asset(s) → ${out}`)
